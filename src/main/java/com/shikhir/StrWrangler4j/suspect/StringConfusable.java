	/** Replaces international characters that are used to escape filters(spam, profanity) back to english characters
	 * 
	 * @param body the text that needs to be 
	 * @return A string unconfused back in english
	 */

	public static String unconfuse(String body) {

		if(map.size()==0) getInstance();
		
		for(int i=0; i < body.length(); i++) {
			int cp = body.codePointAt(i);	
			
			String testStr = new String(Character.toChars(cp));
			
			if (map.containsKey(testStr)) {
				body = StringUtils.replace(body, testStr, map.get(testStr));
			}
		}
		return body;
	}

	
	public static StringConfusable getInstance() {
		if(INSTANCE==null) {
			INSTANCE = new StringConfusable();
		}

		return INSTANCE;
	}
	
